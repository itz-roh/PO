package ggc.app.main;

import pt.tecnico.uilib.menus.Command;
import pt.tecnico.uilib.menus.CommandException;
import ggc.core.WarehouseManager;
import ggc.app.main.Message;

/**
 * Show global balance.
 */
class DoShowGlobalBalance extends Command<WarehouseManager> {

  DoShowGlobalBalance(WarehouseManager receiver) {
    super(Label.SHOW_BALANCE, receiver);
  }

  @Override
  public final void execute() throws CommandException {
    double balance = _receiver.getBalance();
    double accountableBalance = _receiver.getAccountableBalance();
    _display.addLine(Message.currentBalance(balance,accountableBalance));
    _display.display();
    }

  }
  
